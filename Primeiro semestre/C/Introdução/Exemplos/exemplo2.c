/*
Exemplo de variáveis
*/

/* 
    inteiro = int = %i
    real = float = %f
*/

//  INICIO
#include <stdio.h>
#include <stdlib.h>

int main(void){
//  declaração de variáveis
    int n1, n2;

//  atribuir um valor (=)
    n1 = 2;
    n2 = 4;

//  saída de dados
    printf("n1 = %i\n", n1);
    printf("n2 = %i\n", n2);

    return 0;
}